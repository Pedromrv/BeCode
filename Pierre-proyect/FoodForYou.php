<h2>The Raw Food Diet: A Beginner's Guide and Review</h2>
  <p>

The raw food diet has been around since the 1800s, but has surged in popularity in recent years.
Its supporters believe that consuming mostly raw foods is ideal for human health and has many benefits,
 including weight loss and better overall health.<br>
However, health experts warn that eating a mostly raw diet may lead to negative health consequences.
This article reviews the good and bad of the raw food diet, as well as how it works.
</p>
<div id=imgcntr><img src="images/i-healed-6-year-illness-changing-diet.jpg" alt=""></div>
<h2>What Is the Raw Food Diet?</h2>
<p>
The raw food diet, often called raw foodism or raw veganism, is composed of mostly or
completely raw and unprocessed foods.
A food is considered raw if it has never been heated over 104–118°F (40–48°C).
It should also not be refined, pasteurized, treated with pesticides or otherwise processed in any way.
Instead, the diet allows several alternative preparation methods, such as juicing, blending, dehydrating,
 soaking and sprouting. Similar to veganism, the raw food diet is usually plant-based,
being made up mostly of fruits, vegetables, nuts and seeds.<br>
While most raw food diets are completely plant-based, some people also consume raw eggs and dairy.
Less commonly, raw fish and meat may be included as well.
Additionally, taking supplements is typically discouraged on the raw food diet.
 Proponents often claim that the diet will give you all the nutrients you need.
Supporters also believe that cooking foods is harmful to human health because it destroys the natural enzymes in foods,
reduces their nutrient content and reduces the "life force" that they believe to exist in all raw or "living" foods.
People follow the raw food diet for the benefits they believe it has, including weight loss, improved vitality,
increased energy, improvement to chronic diseases, improved overall health and a reduced impact on the environment.
</p>
<div id=imgdwn><img src="images/images.jpeg" alt=""></div>
<h2>How to Follow the Raw Food Diet</h2>
<p>To follow the raw food diet, make sure at least 75% of the food you eat is raw.<br>
Most raw food diets are made primarily of fruits, vegetables, nuts and seeds. Grains and legumes are often permitted as well,
but usually need to be soaked or sprouted before you eat them.</p>
